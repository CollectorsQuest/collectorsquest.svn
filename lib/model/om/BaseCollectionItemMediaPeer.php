	/**
	 * Selects a collection of CollectionItemMedia objects pre-filled with their CollectionItem objects.
	 * @param      Criteria  $c
	 * @param      PropelPDO $con
	 * @param      String    $join_behavior the type of joins to use, defaults to Criteria::LEFT_JOIN
	 * @return     array Array of CollectionItemMedia objects.
	 * @throws     PropelException Any exceptions caught during processing will be
	 *		 rethrown wrapped into a PropelException.
	 */
	public static function doSelectJoinCollectionItem(Criteria $c, $con = null, $join_behavior = Criteria::LEFT_JOIN)
	{
		$c = clone $c;

		// Set the correct dbName if it has not been overridden
		if ($c->getDbName() == Propel::getDefaultDB()) {
			$c->setDbName(self::DATABASE_NAME);
		}

		CollectionItemMediaPeer::addSelectColumns($c);
		$startcol = (CollectionItemMediaPeer::NUM_COLUMNS - CollectionItemMediaPeer::NUM_LAZY_LOAD_COLUMNS);
		CollectionItemPeer::addSelectColumns($c);

		$c->addJoin(array(CollectionItemMediaPeer::ITEM_ID,), array(CollectionItemPeer::ID,), $join_behavior);
		$stmt = BasePeer::doSelect($c, $con);
		$results = array();

		while ($row = $stmt->fetch(PDO::FETCH_NUM)) {
			$key1 = CollectionItemMediaPeer::getPrimaryKeyHashFromRow($row, 0);
			if (null !== ($obj1 = CollectionItemMediaPeer::getInstanceFromPool($key1))) {
				// We no longer rehydrate the object, since this can cause data loss.
				// See http://propel.phpdb.org/trac/ticket/509
				// $obj1->hydrate($row, 0, true); // rehydrate
			} else {

				$omClass = CollectionItemMediaPeer::getOMClass();

				$cls = substr('.'.$omClass, strrpos('.'.$omClass, '.') + 1);
				$obj1 = new $cls();
				$obj1->hydrate($row);
				CollectionItemMediaPeer::addInstanceToPool($obj1, $key1);
			} // if $obj1 already loaded

			$key2 = CollectionItemPeer::getPrimaryKeyHashFromRow($row, $startcol);
			if ($key2 !== null) {
				$obj2 = CollectionItemPeer::getInstanceFromPool($key2);
				if (!$obj2) {

					$omClass = CollectionItemPeer::getOMClass();

					$cls = substr('.'.$omClass, strrpos('.'.$omClass, '.') + 1);
					$obj2 = new $cls();
					$obj2->hydrate($row, $startcol);
					CollectionItemPeer::addInstanceToPool($obj2, $key2);
				} // if obj2 already loaded

				// Add the $obj1 (CollectionItemMedia) to $obj2 (CollectionItem)
				$obj2->addCollectionItemMedia($obj1);

			} // if joined row was not null

			$results[] = $obj1;
		}
		$stmt->closeCursor();
		return $results;
	}


